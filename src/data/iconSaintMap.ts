export function findIconSaintBySlug(slug: string): IconSaintEntry | undefined {
  return iconSaintEntries.find((e) => e.saintSlug === slug);
}

export function iconSaintByCategory(
  category: IconSaintEntry["category"]
): IconSaintEntry[] {
  return iconSaintEntries.filter((e) => e.category === category);
}

export function iconSaintByTradition(
  tradition: IconSaintEntry["tradition"] | "shared"
): IconSaintEntry[] {
  if (tradition === "shared") return iconSaintEntries;
  return iconSaintEntries.filter(
    (e) => e.tradition === tradition || e.tradition === "shared"
  );
}
